    public ArrayList<Empleado> obtenerLista(){
        try {
            String query = "select nombreEmpleado from empleado";
            Statement stat = cn.createStatement();
            conjuntoResultados = stat.executeQuery(query);
            ArrayList<Empleado> info = new ArrayList<Empleado>();
            if(conjuntoResultados.next()){
                do{
                    Empleado listaTemporal = new Empleado (conjuntoResultados.getString(1));
                    info.add(listaTemporal);
                } while (conjuntoResultados.next());
                return info;
            }else{
                return null;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
            return null;
        }
    }
    
    private void cargarComboBox(){
        ArrayList<Empleado> lista = obtenerLista();
        for(int x=0; x<lista.size(); x++){
            Empleado obj = new Empleado(lista.get(x).getNombreEmpleado(),lista.get(x).getIdEmpleado());
            nombreEmpleado.addItem(obj);
        }
    }   
    
